# Lista Vacia
# La longitu de una lista Vacia:
lista_vacia = []
print("Longitud de una lisya vacia: ", len(lista_vacia))

# Lista con enteros
# La longitud es igual al numero de elementos que tiene la lista
lista_enteros = [1, 2, 3, 4,5]
print("La longitud de una lista de enteros es de:", len(lista_enteros))

# Lista con cadenas
# La longitud es igual al numero de elementos de la lista
lista_cadenas = ["manzana", "banana", "cereza"]
print("Longitud de una lista de cadenas es de: ", len(lista_cadenas))

# Lista Mixta
# La longitud cuenta todos los elementos (sin importar el tipo de elementos)
lista_mixta = [1, "texto", 3.14, [1, 2, "texto", 3, 4]]
print("Longitud de una lista mixta es de: ", len(lista_mixta))

# Lista con elementos repetidos
# La longitud es el resultado de multiplicar el numero de elementos repetidos
lista_repetidos = [0] * 10
print("La longitud de una lista de numeros es de: ", len(lista_repetidos))